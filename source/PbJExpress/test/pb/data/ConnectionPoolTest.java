    //测试数据库连接按钮事件
    private void testConnectButtonMouseClicked(java.awt.event.MouseEvent evt)
    {//GEN-FIRST:event_testConnectButtonMouseClicked
        //获取连接属性值
        String url=this.connectUrlTextArea.getText();
        String username=this.userNameTextField.getText();
        char[] userpass=this.userPasswordField.getPassword();

        //连接数据库
        Connector connecter=new PoolJdbcConnector(url,username,String.valueOf(userpass));
        connecter.connect();
        if(connecter.isConnected())
        {
            this.statusDisplayLabel.setText("！测试连接成功");
        }
        else
        {
            this.statusDisplayLabel.setText("！"+connecter.getError());
        }

        connecter.close();
        connecter=null;
    }//GEN-LAST:event_testConnectButtonMouseClicked

    //创建连接池按钮事件
    private void createPoolButtonMouseClicked(java.awt.event.MouseEvent evt)
    {//GEN-FIRST:event_createPoolButtonMouseClicked
        //获取连接属性值
        String url=this.connectUrlTextArea.getText();
        String username=this.userNameTextField.getText();
        char[] userpass=this.userPasswordField.getPassword();

        this.pool=new ConnectionPool(url,username,String.valueOf(userpass));
        this.pool.setInitialConnectNumber(5);
        this.pool.setIncrementalConnectNumber(1);
        this.pool.setMaxConnectNumber(8);
        this.pool.setMaxWaitTime(0);
        this.pool.initConnectionPool();

        this.statusDisplayLabel.setText(
                "总数-"+this.pool.getCurrentTotalConnectNumber()
                        +";空闲数-"+this.pool.getCurrentIdleThreadNumber()
        );
    }//GEN-LAST:event_createPoolButtonMouseClicked

