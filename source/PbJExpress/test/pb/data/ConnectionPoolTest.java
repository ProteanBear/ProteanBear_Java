    //获取空闲连接处理方法
    private PoolConnector connectHandle
    (JButton connect,JButton close,JLabel label,PoolConnector connector)
    {
        //获取空闲连接
        connector=this.pool.getConnection();
        boolean success=(connector!=null);

        if(success)
        {
            //设置按钮状态
            connect.setEnabled(false);
            close.setEnabled(true);

            //显示连接状态
            label.setText("已连接！");

            //显示缓存区状态
            this.statusDisplayLabel.setText(
                    "总数-"+this.pool.getCurrentTotalConnectNumber()
                            +";空闲数-"+this.pool.getCurrentIdleThreadNumber()
            );
        }
        else
        {
            //设置按钮状态
            connect.setEnabled(true);
            close.setEnabled(false);

            //显示连接状态
            label.setText("未连接！");

            //显示缓存区状态
            this.statusDisplayLabel.setText(this.pool.getError());
        }


        return connector;
    }

    //关闭空闲连接处理方法
    private void closeHandle
    (JButton connect,JButton close,JLabel label,PoolConnector connector)
    {
        //获取空闲连接
        this.pool.returnConnection(connector);
        connector=null;

        //设置按钮状态
        connect.setEnabled(true);
        close.setEnabled(false);

        //显示连接状态
        label.setText("未连接！");

        //显示缓存区状态
        this.statusDisplayLabel.setText(
                "总数-"+this.pool.getCurrentTotalConnectNumber()
                        +";空闲数-"+this.pool.getCurrentIdleThreadNumber()
        );
    }

